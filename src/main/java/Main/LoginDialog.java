    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       StringBuilder sb=new StringBuilder();
        DataValidator.empty_Data(txtUserName, sb, "Tên đăng nhập không được để trống");
        DataValidator.empty_Data(txtPassword, sb, "Mật Khẩu không được để trống");
        if(sb.length()>0)
        {
            JOptionPane.showMessageDialog(this, sb.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            TaiKhoan tk=TaiKhoanDao.checkLogin(txtUserName.getText(), new String(txtPassword.getPassword()))  ;
             if(tk==null ) MessageDialogHelper.showErrorDialog(this, "Tên Đăng Nhập hoặc Mật Khẩu chưa đúng!", "lỗi");
             
             else
             {
                 ShareData.taikhoandangnhap=tk;
               //  System.out.println("Ten Dang Nhap:"+tk.getTenDangNhap());
                 
                 this.dispose();
                 TrangChu trangChu=new TrangChu();
                 trangChu.setVisible(rootPaneCheckingEnabled);
             }
                 
       
       
            }
        catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
       
    }//GEN-LAST:event_btnLoginActionPerformed

